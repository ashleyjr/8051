#include "uart.h"


void main (void){       
   long a, b, c; 

   for(a=1000;a<1003;a++){
      for(b=1000;b<1003;b++){
      
         c = a * b;     
  
         uart_tx(c >> 24);
         uart_tx(c >> 16);
         uart_tx(c >> 8); 
         uart_tx(c);
      }
   }
   while(1); 
}

